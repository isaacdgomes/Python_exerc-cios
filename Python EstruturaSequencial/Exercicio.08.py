#Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. Calcule e mostre o total do seu salário no referido mês.

quanto_ganha_por_hora = float(input("Quanto você ganha por hora:"))

numero_horas_trabalhadas = float(input("Número de horas trabalhadas no mês:"))

salario = quanto_ganha_por_hora * numero_horas_trabalhadas

print("Total do seu salário:",salario)

